package PTC;
public class usecharAt_Method
{
	public static void main(String[] args) 
	{
		String str1 ="Mayuri";
		char chh  = str1.charAt(0);
		System.out.println(chh);
	
		String str = "Akshay";
		for(int i=0;i<str.length();i++)
		{
			char ch = str.charAt(i);
			System.out.println(ch);
		}
		
	}
	

}
